#!/usr/bin/env python3
"""
type-annotated function floor which takes a float
n as argument and returns the floor of the float.
"""


def floor(n: float) -> int:
    """
    Returns the largest integer less than or equal to a float number.

    Args:
        n (float): The input floating-point number.

    Returns:
        int: The largest integer less than or equal to n.
    """
    return int(n)
